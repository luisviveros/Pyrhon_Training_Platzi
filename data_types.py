#Tipos de datos en Python

#string
my_name = "Nicolas"
my_name = 'Santiago'
print('my_name=>',my_name)

#Funcion para saber que tipo de variable es:
print(type(my_name))

#int
my_age = 12 
print('my age=>',my_age)
print(type(my_age))

#boolean- (primera letra en mayuscula => True)
is_single = True
print('Is single=>',is_single)
print(type(is_single))

#inputs
my_age = input('Cual es tu edad?')
print('my age=>',my_age)
print(type(my_age))


